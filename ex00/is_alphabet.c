#include <stdio.h>

int	is_alphabet(char *c)
{
	printf("Hello world!");
	while (*c != '\0')
	{
		if (('a' <= *c && *c <= 'z') == 0
			&& ('A' <= *c && *c <= 'Z') == 0)
			return (0);
		c++;
	}
	return (1);
}
